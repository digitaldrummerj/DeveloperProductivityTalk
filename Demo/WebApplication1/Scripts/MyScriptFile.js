//Summary: Add 2 Numbers
//Parameters:
//- firstNumber: the 1st Number to Add
//- secondNumber: the 2nd Number to Add
//Returns: the result of adding the 2 numbers
function add ( firstNumber, secondNumber ) {
	return firstNumber + secondNumber;
}

//Summary: Substract 2 Numbers
//Parameters:
//- firstNumber: the number to substract from the 2nd number
//- secondNumber: the number used to subtract the first number from
//Returns: the result of substracting the firstNumber from the secondNumber
function substract ( firstNumber, secondNumber ) {
	return secondNumber - firstNumber;
}

function multiple ( firstNumber, secondNumber ) {
	return firstNumber * secondNumber;
}

function divide ( firstNumber, secondNumber ) {
	return secondNumber / firstNumber;
}
